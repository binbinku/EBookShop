package com.example.ebookshop.entity;

public class BookUnit
{
    private int bno;
    private int bprice;
    private int bcategory;
    private String bname;

    private String bauthor;
    private String binfo;
    private String bpubdate;
    private String burl;

    public int getBno()
    {
        return bno;
    }

    public void setBno(int bno)
    {
        this.bno = bno;
    }

    public int getBprice()
    {
        return bprice;
    }

    public void setBprice(int bprice)
    {
        this.bprice = bprice;
    }

    public int getBcategory()
    {
        return bcategory;
    }

    public void setBcategory(int bcategory)
    {
        this.bcategory = bcategory;
    }

    public String getBname()
    {
        return bname;
    }

    public void setBname(String bname)
    {
        this.bname = bname;
    }

    public String getBauthor()
    {
        return bauthor;
    }

    public void setBauthor(String bauthor)
    {
        this.bauthor = bauthor;
    }

    public String getBinfo()
    {
        return binfo;
    }

    public void setBinfo(String binfo)
    {
        this.binfo = binfo;
    }



    public String getBurl()
    {
        return burl;
    }

    public void setBurl(String burl)
    {
        this.burl = burl;
    }

    public String getBpubdate()
    {
        return bpubdate;
    }

    public void setBpubdate(String bpubdate)
    {
        this.bpubdate = bpubdate;
    }
}
